    private void botonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAgregarActionPerformed
        String apaterno = aPaterno.getText();
        String amaterno = aMaterno.getText();
        String nacimiento = fechaNacimiento.getText();
        String nom = nombre.getText();
        String alta = fechaAlta.getText();
        String genero = sexo.getSelectedItem().toString();
        String tipous = tipoUsuario.getSelectedItem().toString();
        String pass = password.getText();
        String usu = usuario.getText();
        if(sexo.getSelectedIndex()==0 && tipoUsuario.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(null, "Seleccione un tipo de usuario y sexo");
        }
        else{
            if(sexo.getSelectedIndex()==0){
                JOptionPane.showMessageDialog(null, "Seleccione un sexo");
            }
            else{
                if(tipoUsuario.getSelectedIndex()==0){
                    JOptionPane.showMessageDialog(null, "Seleccione un tipo de usuario");
                } 
                else{
                    if(apaterno.contentEquals("")){
                        JOptionPane.showMessageDialog(null, "Proporcione apellido paterno");
                    }
                    else{
                        if(nom.contentEquals("")){
                            JOptionPane.showMessageDialog(null, "Proporcione nombre(s)");
                        }
                        else{
                            if(nacimiento.contentEquals("")){
                                JOptionPane.showMessageDialog(null, "Proporcione fecha de nacimiento");
                            }
                            else{
                                if(alta.contentEquals("")){
                                    JOptionPane.showMessageDialog(null, "Proporcione fecha de ingreso");
                                }
                                else{
                                    base.agregarUsuarios(apaterno, amaterno, nacimiento, nom, alta, genero, tipous, pass, usu);
                                }
                            }
                        }
                    }
                }
            }
        }
    }//GEN-LAST:event_botonAgregarActionPerformed

