    private void botonConsultaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonConsultaActionPerformed
        String idpaci = idPaciente.getText();
        String pa = base.buscarPaciente(idpaci);
        nombrePaciente.setText(pa);
    }//GEN-LAST:event_botonConsultaActionPerformed

    private void botonRegresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonRegresarActionPerformed
        if(botonregresar ==null){
            botonregresar = new Administrar_citas();
            botonregresar.setVisible(true);
            setVisible(false);           
        }
        else{
            botonregresar.setVisible(true); 
            setVisible(false);
        }
    }//GEN-LAST:event_botonRegresarActionPerformed

    private void botonAgendarCitaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAgendarCitaActionPerformed
        String paci = idPaciente.getText();
        String horacita = hora.getText()+""+horario.getSelectedItem().toString();;
        String fechaactual = fechaActual.getText();
        String fechacita = agregarFechaBD();
        String idusu = idUsuario.getText();
        base.agendarCita(paci, horacita, fechaactual, fechacita, idusu);
    }//GEN-LAST:event_botonAgendarCitaActionPerformed

    private void botonConsultarMedicoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonConsultarMedicoActionPerformed
        String idusu = idUsuario.getText();
        String me = base.idMedico(idusu);
        nombreMedico.setText(me);
    }//GEN-LAST:event_botonConsultarMedicoActionPerformed

