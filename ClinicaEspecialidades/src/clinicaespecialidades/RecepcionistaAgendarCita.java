    private String agregarFechaBD(){
        Date fechaBD = fechaAsignar.getDate();
        SimpleDateFormat formato = new SimpleDateFormat("yyyy/MM/dd");
        System.out.println(formato.format(fechaBD));  
        String fecha = formato.format(fechaBD);
        return fecha;
    }
